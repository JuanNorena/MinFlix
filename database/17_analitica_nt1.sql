-- ============================================================================
-- SECCION A: Fragmentacion (Particionamiento por rango de FECHA_INICIO)
-- ============================================================================
-- Oracle no permite agregar particionamiento a una tabla existente.
-- Estrategia: guardar datos, recrear la tabla con particiones, reinsertar.
-- Se usan cinco particiones por ventanas de tiempo para optimizar consultas
-- analiticas que filtran por periodo (WHERE FECHA_INICIO BETWEEN ...).
-- ============================================================================
-- Importante: el backup REPRODUCCIONES_BKP permite rollback manual si falla.

-- Paso A.1: Guardar datos existentes en tabla temporal de respaldo
CREATE TABLE REPRODUCCIONES_BKP AS
  SELECT * FROM REPRODUCCIONES;

-- Paso A.2: Eliminar tabla original con sus constraints e indices
--   CASCADE CONSTRAINTS elimina las FKs propias (PERFILES, CONTENIDOS).
--   El trigger TRG_REPRODUCCIONES_REGLAS_BIU se elimina automaticamente.
--   La vista VW_CONTINUAR_VIENDO quedara invalida y se recompilara al recrear la tabla.
DROP TABLE REPRODUCCIONES CASCADE CONSTRAINTS;

-- Paso A.3: Recrear tabla con PARTITION BY RANGE sobre FECHA_INICIO.
-- Cada particion almacena fisicamente las reproducciones de un periodo de tiempo,
-- acelerando consultas analiticas que filtran por rango de fechas.
CREATE TABLE REPRODUCCIONES (
  -- PK autogenerada: identificador unico de cada sesion de reproduccion
  ID_REPRODUCCION         NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  -- FK al perfil que realiza la reproduccion (obligatorio)
  ID_PERFIL               NUMBER NOT NULL,
  -- FK al contenido reproducido (obligatorio)
  ID_CONTENIDO            NUMBER NOT NULL,
  -- Segundos visionados hasta el momento (no puede ser negativo)
  PROGRESO_SEGUNDOS       NUMBER(10,0) DEFAULT 0 NOT NULL,
  -- Duracion total del contenido en segundos (NULL si no se conoce)
  DURACION_TOTAL_SEGUNDOS NUMBER(10,0) NULL,
  -- Porcentaje de visionado calculado automaticamente por trigger (0-100)
  PORCENTAJE_AVANCE       NUMBER(5,2) DEFAULT 0 NOT NULL,
  -- Dispositivo desde el que se reprodujo (tv, movil, computador, tablet)
  ULTIMO_DISPOSITIVO      VARCHAR2(80) NULL,
  -- Estado de la sesion: EN_PROGRESO, PAUSADO o FINALIZADO
  ESTADO_REPRODUCCION     VARCHAR2(20) DEFAULT 'EN_PROGRESO' NOT NULL,
  -- Fecha y hora de inicio de la reproduccion (clave de particionamiento)
  FECHA_INICIO            TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
  -- Fecha y hora del ultimo evento recibido (heartbeat/pausa/progreso)
  FECHA_ULTIMO_EVENTO     TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
  -- Fecha y hora en que la reproduccion llego a FINALIZADO (NULL si aun no)
  FECHA_FIN               TIMESTAMP NULL,
  -- Constraint: el progreso no puede ser negativo
  CONSTRAINT CK_REPRO_PROGRESO  CHECK (PROGRESO_SEGUNDOS >= 0),
  -- Constraint: si hay duracion total, debe ser mayor que cero
  CONSTRAINT CK_REPRO_DURACION  CHECK (
    DURACION_TOTAL_SEGUNDOS IS NULL OR DURACION_TOTAL_SEGUNDOS > 0
  ),
  -- Constraint: el porcentaje de avance debe estar entre 0 y 100
  CONSTRAINT CK_REPRO_PORCENTAJE CHECK (PORCENTAJE_AVANCE BETWEEN 0 AND 100),
  -- Constraint: solo valores validos de estado de reproduccion
  CONSTRAINT CK_REPRO_ESTADO    CHECK (
    ESTADO_REPRODUCCION IN ('EN_PROGRESO', 'PAUSADO', 'FINALIZADO')
  ),
  -- FK: el perfil debe existir en la tabla PERFILES
  CONSTRAINT FK_REPRO_PERFIL    FOREIGN KEY (ID_PERFIL)
    REFERENCES PERFILES(ID_PERFIL),
  -- FK: el contenido debe existir en la tabla CONTENIDOS
  CONSTRAINT FK_REPRO_CONTENIDO FOREIGN KEY (ID_CONTENIDO)
    REFERENCES CONTENIDOS(ID_CONTENIDO)
)
-- Particionamiento por rango sobre FECHA_INICIO para consultas analiticas rapidas.
-- Oracle redirige la consulta a la particion relevante sin escanear toda la tabla.
PARTITION BY RANGE (FECHA_INICIO) (
  -- Partition P_2024: reproducciones del ano 2024 (hasta 2025-01-01 exclusive)
  PARTITION P_2024    VALUES LESS THAN (TIMESTAMP '2025-01-01 00:00:00'),
  -- Partition P_2025_Q1: primer trimestre de 2025 (hasta 2025-04-01 exclusive)
  PARTITION P_2025_Q1 VALUES LESS THAN (TIMESTAMP '2025-04-01 00:00:00'),
  -- Partition P_2025_Q2: segundo trimestre de 2025 (hasta 2025-07-01 exclusive)
  PARTITION P_2025_Q2 VALUES LESS THAN (TIMESTAMP '2025-07-01 00:00:00'),
  -- Partition P_2025_Q3: tercer trimestre de 2025 (hasta 2025-10-01 exclusive)
  PARTITION P_2025_Q3 VALUES LESS THAN (TIMESTAMP '2025-10-01 00:00:00'),
  -- Partition P_FUTURO: cuarto trimestre 2025 y todas las fechas posteriores
  PARTITION P_FUTURO  VALUES LESS THAN (MAXVALUE)
);

-- Paso A.4: Reinsertar datos del respaldo en la tabla particionada
-- Mantiene IDs originales para no romper referencias en historiales.
INSERT INTO REPRODUCCIONES (
  ID_REPRODUCCION, ID_PERFIL, ID_CONTENIDO,
  PROGRESO_SEGUNDOS, DURACION_TOTAL_SEGUNDOS, PORCENTAJE_AVANCE,
  ULTIMO_DISPOSITIVO, ESTADO_REPRODUCCION,
  FECHA_INICIO, FECHA_ULTIMO_EVENTO, FECHA_FIN
)
SELECT
  ID_REPRODUCCION, ID_PERFIL, ID_CONTENIDO,
  PROGRESO_SEGUNDOS, DURACION_TOTAL_SEGUNDOS, PORCENTAJE_AVANCE,
  ULTIMO_DISPOSITIVO, ESTADO_REPRODUCCION,
  FECHA_INICIO, FECHA_ULTIMO_EVENTO, FECHA_FIN
FROM REPRODUCCIONES_BKP;

COMMIT;

