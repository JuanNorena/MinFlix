CREATE TABLE REPORTES (
  ID_REPORTE NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  ID_PERFIL_REPORTADOR NUMBER NOT NULL,
  ID_CONTENIDO NUMBER NOT NULL,
  MOTIVO VARCHAR2(120) NOT NULL,
  DETALLE VARCHAR2(1000) NULL,
  ESTADO_REPORTE VARCHAR2(20) DEFAULT 'ABIERTO' NOT NULL,
  ID_USUARIO_MODERADOR NUMBER NULL,
  RESOLUCION VARCHAR2(1000) NULL,
  FECHA_REPORTE TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
  FECHA_ACTUALIZACION TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
  FECHA_RESOLUCION TIMESTAMP NULL,
  CONSTRAINT CK_REPORTES_ESTADO CHECK (
    ESTADO_REPORTE IN ('ABIERTO', 'EN_REVISION', 'RESUELTO', 'DESCARTADO')
  ),
  CONSTRAINT FK_REPORTES_PERFIL FOREIGN KEY (ID_PERFIL_REPORTADOR)
    REFERENCES PERFILES(ID_PERFIL),
  CONSTRAINT FK_REPORTES_CONTENIDO FOREIGN KEY (ID_CONTENIDO)
    REFERENCES CONTENIDOS(ID_CONTENIDO),
  CONSTRAINT FK_REPORTES_MODERADOR FOREIGN KEY (ID_USUARIO_MODERADOR)
    REFERENCES USUARIOS(ID_USUARIO)
);

CREATE INDEX IDX_REPORTES_ESTADO_FECHA
  ON REPORTES (ESTADO_REPORTE, FECHA_ACTUALIZACION DESC);

CREATE INDEX IDX_REPORTES_CONTENIDO_FECHA
  ON REPORTES (ID_CONTENIDO, FECHA_REPORTE DESC);

CREATE INDEX IDX_REPORTES_PERFIL_FECHA
  ON REPORTES (ID_PERFIL_REPORTADOR, FECHA_REPORTE DESC);

CREATE OR REPLACE TRIGGER TRG_REPORTES_REGLAS_BIU
BEFORE INSERT OR UPDATE ON REPORTES
FOR EACH ROW
DECLARE
  V_EXISTE NUMBER;
  V_ROL_MODERADOR USUARIOS.ROL%TYPE;
BEGIN
  SELECT COUNT(*)
    INTO V_EXISTE
    FROM PERFILES
   WHERE ID_PERFIL = :NEW.ID_PERFIL_REPORTADOR;

  IF V_EXISTE = 0 THEN
    RAISE_APPLICATION_ERROR(
      -20061,
      'No existe el perfil reportador asociado al reporte'
    );
  END IF;

  SELECT COUNT(*)
    INTO V_EXISTE
    FROM CONTENIDOS
   WHERE ID_CONTENIDO = :NEW.ID_CONTENIDO;

  IF V_EXISTE = 0 THEN
    RAISE_APPLICATION_ERROR(
      -20062,
      'No existe el contenido asociado al reporte'
    );
  END IF;

  IF :NEW.ID_USUARIO_MODERADOR IS NOT NULL THEN
    SELECT U.ROL
      INTO V_ROL_MODERADOR
      FROM USUARIOS U
     WHERE U.ID_USUARIO = :NEW.ID_USUARIO_MODERADOR;

    IF LOWER(V_ROL_MODERADOR) NOT IN ('soporte', 'admin') THEN
      RAISE_APPLICATION_ERROR(
        -20063,
        'Solo usuarios con rol soporte o admin pueden moderar reportes'
      );
    END IF;
  END IF;

  IF UPDATING THEN
    IF :OLD.ESTADO_REPORTE IN ('RESUELTO', 'DESCARTADO')
       AND :NEW.ESTADO_REPORTE <> :OLD.ESTADO_REPORTE THEN
      RAISE_APPLICATION_ERROR(
        -20064,
        'No se permite reabrir reportes cerrados'
      );
    END IF;
  END IF;

  :NEW.FECHA_ACTUALIZACION := CURRENT_TIMESTAMP;

  IF :NEW.ESTADO_REPORTE IN ('RESUELTO', 'DESCARTADO') THEN
    IF :NEW.ID_USUARIO_MODERADOR IS NULL THEN
      RAISE_APPLICATION_ERROR(
        -20065,
        'Para cerrar un reporte se requiere moderador asignado'
      );
    END IF;

    IF :NEW.FECHA_RESOLUCION IS NULL THEN
      :NEW.FECHA_RESOLUCION := CURRENT_TIMESTAMP;
    END IF;
  ELSIF :NEW.ESTADO_REPORTE = 'ABIERTO' THEN
    :NEW.FECHA_RESOLUCION := NULL;
    :NEW.RESOLUCION := NULL;
  END IF;
EXCEPTION
  WHEN NO_DATA_FOUND THEN
    RAISE_APPLICATION_ERROR(
      -20066,
      'No existe el usuario moderador asociado al reporte'
    );
END;
/

CREATE OR REPLACE VIEW VW_REPORTES_PENDIENTES_SOPORTE AS
SELECT
  R.ID_REPORTE,
  R.ID_PERFIL_REPORTADOR,
  P.NOMBRE AS NOMBRE_PERFIL_REPORTADOR,
  R.ID_CONTENIDO,
  C.TITULO,
  R.MOTIVO,
  R.DETALLE,
  R.ESTADO_REPORTE,
  R.ID_USUARIO_MODERADOR,
  R.FECHA_REPORTE,
  R.FECHA_ACTUALIZACION
FROM REPORTES R
INNER JOIN PERFILES P ON P.ID_PERFIL = R.ID_PERFIL_REPORTADOR
INNER JOIN CONTENIDOS C ON C.ID_CONTENIDO = R.ID_CONTENIDO
WHERE R.ESTADO_REPORTE IN ('ABIERTO', 'EN_REVISION');

COMMIT;
