      UPDATE FACTURACIONES
         SET FECHA_CORTE = P_FECHA_CORTE,
             FECHA_VENCIMIENTO = P_FECHA_VENC,
             MONTO_BASE = P_MONTO_BASE,
             DESCUENTO_REFERIDOS_PCT = P_DESC_REF,
             DESCUENTO_FIDELIDAD_PCT = P_DESC_FID,
             ESTADO_FACTURA = UPPER(P_ESTADO_FACTURA)
       WHERE ID_FACTURACION = V_ID_FACTURA;
    EXCEPTION
      WHEN NO_DATA_FOUND THEN
        INSERT INTO FACTURACIONES (
          ID_USUARIO,
          PERIODO_ANIO,
          PERIODO_MES,
          FECHA_CORTE,
          FECHA_VENCIMIENTO,
          MONTO_BASE,
          DESCUENTO_REFERIDOS_PCT,
          DESCUENTO_FIDELIDAD_PCT,
          MONTO_FINAL,
          ESTADO_FACTURA
        ) VALUES (
          V_ID_USUARIO,
          P_ANIO,
          P_MES,
          P_FECHA_CORTE,
          P_FECHA_VENC,
          P_MONTO_BASE,
          P_DESC_REF,
          P_DESC_FID,
          P_MONTO_BASE,
          UPPER(P_ESTADO_FACTURA)
        );
    END;
  END;

  PROCEDURE UPSERT_PAGO (
    P_REFERENCIA   IN VARCHAR2,
    P_EMAIL        IN VARCHAR2,
    P_ANIO         IN NUMBER,
    P_MES          IN NUMBER,
    P_MONTO        IN NUMBER,
    P_METODO       IN VARCHAR2,
    P_ESTADO       IN VARCHAR2
  )
  IS
    V_ID_USUARIO   USUARIOS.ID_USUARIO%TYPE;
    V_ID_FACTURA   FACTURACIONES.ID_FACTURACION%TYPE;
    V_ID_PAGO      PAGOS.ID_PAGO%TYPE;
  BEGIN
    V_ID_USUARIO := GET_USUARIO_ID(P_EMAIL);

    SELECT F.ID_FACTURACION
      INTO V_ID_FACTURA
      FROM FACTURACIONES F
     WHERE F.ID_USUARIO = V_ID_USUARIO
       AND F.PERIODO_ANIO = P_ANIO
       AND F.PERIODO_MES = P_MES;

    BEGIN
      SELECT P.ID_PAGO
        INTO V_ID_PAGO
        FROM PAGOS P
       WHERE UPPER(NVL(P.REFERENCIA_PAGO, 'NA')) = UPPER(NVL(P_REFERENCIA, 'NA'));

      UPDATE PAGOS
         SET ID_FACTURACION = V_ID_FACTURA,
             ID_USUARIO = V_ID_USUARIO,
             MONTO = P_MONTO,
             METODO_PAGO = UPPER(P_METODO),
             ESTADO_TRANSACCION = UPPER(P_ESTADO)
       WHERE ID_PAGO = V_ID_PAGO;
    EXCEPTION
      WHEN NO_DATA_FOUND THEN
        INSERT INTO PAGOS (
          ID_FACTURACION,
          ID_USUARIO,
          MONTO,
          METODO_PAGO,
          ESTADO_TRANSACCION,
          REFERENCIA_PAGO
        ) VALUES (
          V_ID_FACTURA,
          V_ID_USUARIO,
          P_MONTO,
          UPPER(P_METODO),
          UPPER(P_ESTADO),
          P_REFERENCIA
        );
    END;

    IF UPPER(P_ESTADO) = 'EXITOSO' THEN
      UPDATE FACTURACIONES
         SET ESTADO_FACTURA = 'PAGADA',
             FECHA_PAGO = NVL(FECHA_PAGO, CURRENT_TIMESTAMP)
       WHERE ID_FACTURACION = V_ID_FACTURA;

      UPDATE USUARIOS
         SET ESTADO_CUENTA = 'ACTIVO'
       WHERE ID_USUARIO = V_ID_USUARIO;
    END IF;
  END;

