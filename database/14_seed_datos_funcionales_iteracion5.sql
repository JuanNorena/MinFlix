  PROCEDURE UPSERT_REPORTE (
    P_EMAIL_REPORTADOR IN VARCHAR2,
    P_NOMBRE_PERFIL    IN VARCHAR2,
    P_TITULO_CONTENIDO IN VARCHAR2,
    P_MOTIVO           IN VARCHAR2,
    P_DETALLE          IN VARCHAR2,
    P_ESTADO           IN VARCHAR2,
    P_EMAIL_MODERADOR  IN VARCHAR2 DEFAULT NULL,
    P_RESOLUCION       IN VARCHAR2 DEFAULT NULL
  )
  IS
    V_ID_PERFIL       PERFILES.ID_PERFIL%TYPE;
    V_ID_CONTENIDO    CONTENIDOS.ID_CONTENIDO%TYPE;
    V_ID_MODERADOR    USUARIOS.ID_USUARIO%TYPE;
    V_ID_REPORTE      REPORTES.ID_REPORTE%TYPE;
  BEGIN
    V_ID_PERFIL := GET_PERFIL_ID(P_EMAIL_REPORTADOR, P_NOMBRE_PERFIL);
    V_ID_CONTENIDO := GET_CONTENIDO_ID(P_TITULO_CONTENIDO);

    IF P_EMAIL_MODERADOR IS NOT NULL THEN
      V_ID_MODERADOR := GET_USUARIO_ID(P_EMAIL_MODERADOR);
    ELSE
      V_ID_MODERADOR := NULL;
    END IF;

    BEGIN
      SELECT R.ID_REPORTE
        INTO V_ID_REPORTE
        FROM REPORTES R
       WHERE UPPER(NVL(R.DETALLE, 'NA')) = UPPER(NVL(P_DETALLE, 'NA'));

      UPDATE REPORTES
         SET ID_PERFIL_REPORTADOR = V_ID_PERFIL,
             ID_CONTENIDO = V_ID_CONTENIDO,
