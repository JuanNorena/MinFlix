  PROCEDURE UPSERT_RELACION_CONTENIDOS (
    P_TITULO_ORIGEN     IN VARCHAR2,
    P_TITULO_RELACION   IN VARCHAR2,
    P_TIPO_RELACION     IN VARCHAR2,
    P_DESCRIPCION       IN VARCHAR2
  )
  IS
    V_ID_ORIGEN     CONTENIDOS.ID_CONTENIDO%TYPE;
    V_ID_RELACION   CONTENIDOS.ID_CONTENIDO%TYPE;
    V_EXISTE_DIRECTA NUMBER;
    V_EXISTE_INVERSA NUMBER;
  BEGIN
    V_ID_ORIGEN := GET_CONTENIDO_ID(P_TITULO_ORIGEN);
    V_ID_RELACION := GET_CONTENIDO_ID(P_TITULO_RELACION);

    SELECT COUNT(*)
      INTO V_EXISTE_DIRECTA
      FROM CONTENIDOS_RELACIONADOS CR
     WHERE CR.ID_CONTENIDO_ORIGEN = V_ID_ORIGEN
       AND CR.ID_CONTENIDO_RELACIONADO = V_ID_RELACION
       AND CR.TIPO_RELACION = UPPER(P_TIPO_RELACION);

    IF V_EXISTE_DIRECTA = 0 THEN
      SELECT COUNT(*)
        INTO V_EXISTE_INVERSA
        FROM CONTENIDOS_RELACIONADOS CR
       WHERE CR.ID_CONTENIDO_ORIGEN = V_ID_RELACION
         AND CR.ID_CONTENIDO_RELACIONADO = V_ID_ORIGEN
         AND CR.TIPO_RELACION = UPPER(P_TIPO_RELACION);

      IF V_EXISTE_INVERSA = 0 THEN
        INSERT INTO CONTENIDOS_RELACIONADOS (
          ID_CONTENIDO_ORIGEN,
          ID_CONTENIDO_RELACIONADO,
          TIPO_RELACION,
          DESCRIPCION
        ) VALUES (
          V_ID_ORIGEN,
          V_ID_RELACION,
          UPPER(P_TIPO_RELACION),
          P_DESCRIPCION
        );
      END IF;
    END IF;
  END;

  PROCEDURE UPSERT_DEPARTAMENTO (
    P_NOMBRE      IN VARCHAR2,
    P_DESCRIPCION IN VARCHAR2
  )
  IS
    V_EXISTE NUMBER;
  BEGIN
    SELECT COUNT(*)
      INTO V_EXISTE
      FROM DEPARTAMENTOS D
     WHERE UPPER(D.NOMBRE) = UPPER(P_NOMBRE);

    IF V_EXISTE = 0 THEN
      INSERT INTO DEPARTAMENTOS (NOMBRE, DESCRIPCION)
      VALUES (P_NOMBRE, P_DESCRIPCION);
    ELSE
      UPDATE DEPARTAMENTOS
         SET DESCRIPCION = P_DESCRIPCION
       WHERE UPPER(NOMBRE) = UPPER(P_NOMBRE);
    END IF;
  END;

  PROCEDURE UPSERT_EMPLEADO (
    P_EMAIL            IN VARCHAR2,
    P_DEPARTAMENTO     IN VARCHAR2,
    P_CARGO            IN VARCHAR2,
    P_ES_JEFE          IN NUMBER,
    P_SUPERVISOR_EMAIL IN VARCHAR2 DEFAULT NULL
  )
  IS
    V_ID_USUARIO      USUARIOS.ID_USUARIO%TYPE;
    V_ID_DEPTO        DEPARTAMENTOS.ID_DEPARTAMENTO%TYPE;
    V_ID_SUP_USUARIO  USUARIOS.ID_USUARIO%TYPE;
    V_ID_SUPERVISOR   EMPLEADOS.ID_EMPLEADO%TYPE;
    V_ID_EMPLEADO     EMPLEADOS.ID_EMPLEADO%TYPE;
  BEGIN
    V_ID_USUARIO := GET_USUARIO_ID(P_EMAIL);
    V_ID_DEPTO := GET_DEPARTAMENTO_ID(P_DEPARTAMENTO);

    IF P_SUPERVISOR_EMAIL IS NOT NULL THEN
      V_ID_SUP_USUARIO := GET_USUARIO_ID(P_SUPERVISOR_EMAIL);

      SELECT E.ID_EMPLEADO
        INTO V_ID_SUPERVISOR
        FROM EMPLEADOS E
       WHERE E.ID_USUARIO = V_ID_SUP_USUARIO;
    ELSE
      V_ID_SUPERVISOR := NULL;
    END IF;

    BEGIN
      SELECT E.ID_EMPLEADO
        INTO V_ID_EMPLEADO
        FROM EMPLEADOS E
       WHERE E.ID_USUARIO = V_ID_USUARIO;

      UPDATE EMPLEADOS
         SET ID_DEPARTAMENTO = V_ID_DEPTO,
             ID_SUPERVISOR = V_ID_SUPERVISOR,
             CARGO = P_CARGO,
             ES_JEFE = P_ES_JEFE,
             ESTADO_EMPLEADO = 'ACTIVO'
       WHERE ID_EMPLEADO = V_ID_EMPLEADO;
    EXCEPTION
      WHEN NO_DATA_FOUND THEN
        INSERT INTO EMPLEADOS (
          ID_USUARIO,
          ID_DEPARTAMENTO,
          ID_SUPERVISOR,
          CARGO,
          ES_JEFE,
          ESTADO_EMPLEADO
        ) VALUES (
          V_ID_USUARIO,
          V_ID_DEPTO,
          V_ID_SUPERVISOR,
          P_CARGO,
          P_ES_JEFE,
          'ACTIVO'
        );
    END;
  END;

  PROCEDURE UPSERT_REPRODUCCION (
    P_EMAIL            IN VARCHAR2,
    P_NOMBRE_PERFIL    IN VARCHAR2,
    P_TITULO_CONTENIDO IN VARCHAR2,
    P_PROGRESO_SEG     IN NUMBER,
    P_DURACION_SEG     IN NUMBER,
    P_DISPOSITIVO      IN VARCHAR2,
    P_ESTADO           IN VARCHAR2
  )
  IS
    V_ID_PERFIL       PERFILES.ID_PERFIL%TYPE;
    V_ID_CONTENIDO    CONTENIDOS.ID_CONTENIDO%TYPE;
    V_ID_REPRODUCCION REPRODUCCIONES.ID_REPRODUCCION%TYPE;
  BEGIN
    V_ID_PERFIL := GET_PERFIL_ID(P_EMAIL, P_NOMBRE_PERFIL);
    V_ID_CONTENIDO := GET_CONTENIDO_ID(P_TITULO_CONTENIDO);

    SELECT MIN(R.ID_REPRODUCCION)
      INTO V_ID_REPRODUCCION
      FROM REPRODUCCIONES R
     WHERE R.ID_PERFIL = V_ID_PERFIL
       AND R.ID_CONTENIDO = V_ID_CONTENIDO
       AND UPPER(NVL(R.ULTIMO_DISPOSITIVO, 'NA')) = UPPER(NVL(P_DISPOSITIVO, 'NA'));

    IF V_ID_REPRODUCCION IS NULL THEN
      INSERT INTO REPRODUCCIONES (
        ID_PERFIL,
        ID_CONTENIDO,
        PROGRESO_SEGUNDOS,
        DURACION_TOTAL_SEGUNDOS,
        ULTIMO_DISPOSITIVO,
        ESTADO_REPRODUCCION
      ) VALUES (
        V_ID_PERFIL,
        V_ID_CONTENIDO,
        P_PROGRESO_SEG,
        P_DURACION_SEG,
        P_DISPOSITIVO,
        UPPER(P_ESTADO)
      );
    ELSE
      UPDATE REPRODUCCIONES
         SET PROGRESO_SEGUNDOS = P_PROGRESO_SEG,
             DURACION_TOTAL_SEGUNDOS = P_DURACION_SEG,
             ULTIMO_DISPOSITIVO = P_DISPOSITIVO,
             ESTADO_REPRODUCCION = UPPER(P_ESTADO)
       WHERE ID_REPRODUCCION = V_ID_REPRODUCCION;
    END IF;
  END;

