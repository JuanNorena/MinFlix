  FUNCTION GET_USUARIO_ID (
    P_EMAIL IN VARCHAR2
  ) RETURN NUMBER
  IS
    V_ID USUARIOS.ID_USUARIO%TYPE;
  BEGIN
    SELECT U.ID_USUARIO
      INTO V_ID
      FROM USUARIOS U
     WHERE UPPER(U.EMAIL) = UPPER(P_EMAIL);

    RETURN V_ID;
  EXCEPTION
    WHEN NO_DATA_FOUND THEN
      RAISE_APPLICATION_ERROR(
        -20140,
        'No existe usuario para email: ' || P_EMAIL || '. Ejecuta primero script 13.'
      );
  END;

  FUNCTION GET_PERFIL_ID (
    P_EMAIL IN VARCHAR2,
    P_NOMBRE_PERFIL IN VARCHAR2
  ) RETURN NUMBER
  IS
    V_ID_USUARIO USUARIOS.ID_USUARIO%TYPE;
    V_ID_PERFIL  PERFILES.ID_PERFIL%TYPE;
  BEGIN
    V_ID_USUARIO := GET_USUARIO_ID(P_EMAIL);

    BEGIN
      SELECT P.ID_PERFIL
        INTO V_ID_PERFIL
        FROM PERFILES P
       WHERE P.ID_USUARIO = V_ID_USUARIO
         AND UPPER(P.NOMBRE) = UPPER(P_NOMBRE_PERFIL);
    EXCEPTION
      WHEN NO_DATA_FOUND THEN
        SELECT MIN(P.ID_PERFIL)
          INTO V_ID_PERFIL
          FROM PERFILES P
         WHERE P.ID_USUARIO = V_ID_USUARIO;

        IF V_ID_PERFIL IS NULL THEN
          RAISE_APPLICATION_ERROR(
            -20141,
            'El usuario ' || P_EMAIL || ' no tiene perfiles registrados'
          );
        END IF;
    END;

    RETURN V_ID_PERFIL;
  END;

  FUNCTION GET_CATEGORIA_ID (
    P_NOMBRE IN VARCHAR2
  ) RETURN NUMBER
  IS
    V_ID CATEGORIAS.ID_CATEGORIA%TYPE;
  BEGIN
    SELECT C.ID_CATEGORIA
      INTO V_ID
      FROM CATEGORIAS C
     WHERE UPPER(C.NOMBRE) = UPPER(P_NOMBRE);

    RETURN V_ID;
  EXCEPTION
    WHEN NO_DATA_FOUND THEN
      RAISE_APPLICATION_ERROR(
        -20142,
        'No existe categoria: ' || P_NOMBRE
      );
  END;

  FUNCTION GET_CONTENIDO_ID (
    P_TITULO IN VARCHAR2
  ) RETURN NUMBER
  IS
    V_ID CONTENIDOS.ID_CONTENIDO%TYPE;
  BEGIN
    SELECT MIN(C.ID_CONTENIDO)
      INTO V_ID
      FROM CONTENIDOS C
     WHERE UPPER(C.TITULO) = UPPER(P_TITULO);

    IF V_ID IS NULL THEN
      RAISE_APPLICATION_ERROR(
        -20143,
        'No existe contenido: ' || P_TITULO
      );
    END IF;

    RETURN V_ID;
  END;

