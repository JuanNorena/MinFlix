  PROCEDURE UPSERT_PERFIL (
    P_EMAIL       IN VARCHAR2,
    P_NOMBRE      IN VARCHAR2,
    P_TIPO_PERFIL IN VARCHAR2
  )
  IS
    V_ID_USUARIO USUARIOS.ID_USUARIO%TYPE;
    V_EXISTE     NUMBER;
  BEGIN
    V_ID_USUARIO := GET_USUARIO_ID(P_EMAIL);

    SELECT COUNT(*)
      INTO V_EXISTE
      FROM PERFILES P
     WHERE P.ID_USUARIO = V_ID_USUARIO
       AND UPPER(P.NOMBRE) = UPPER(P_NOMBRE);

    IF V_EXISTE = 0 THEN
      INSERT INTO PERFILES (
        ID_USUARIO,
        NOMBRE,
        TIPO_PERFIL
      ) VALUES (
        V_ID_USUARIO,
        P_NOMBRE,
        LOWER(P_TIPO_PERFIL)
      );
    END IF;
  END;

  PROCEDURE UPSERT_GENERO (
    P_NOMBRE      IN VARCHAR2,
    P_DESCRIPCION IN VARCHAR2
  )
  IS
    V_ID GENEROS.ID_GENERO%TYPE;
  BEGIN
    BEGIN
      SELECT G.ID_GENERO
        INTO V_ID
        FROM GENEROS G
       WHERE UPPER(G.NOMBRE) = UPPER(P_NOMBRE);

      UPDATE GENEROS
         SET DESCRIPCION = P_DESCRIPCION
       WHERE ID_GENERO = V_ID;
    EXCEPTION
      WHEN NO_DATA_FOUND THEN
        INSERT INTO GENEROS (
          NOMBRE,
          DESCRIPCION
        ) VALUES (
          P_NOMBRE,
          P_DESCRIPCION
        );
    END;
  END;

  PROCEDURE UPSERT_CONTENIDO (
    P_TITULO          IN VARCHAR2,
    P_TIPO            IN VARCHAR2,
    P_ANIO            IN NUMBER,
    P_DURACION_MIN    IN NUMBER,
    P_SINOPSIS        IN VARCHAR2,
    P_CLASIFICACION   IN VARCHAR2,
    P_ES_EXCLUSIVO    IN NUMBER,
    P_CATEGORIA       IN VARCHAR2,
    P_PUBLICADOR_MAIL IN VARCHAR2
  )
  IS
    V_ID_CONTENIDO   CONTENIDOS.ID_CONTENIDO%TYPE;
    V_ID_CATEGORIA   CATEGORIAS.ID_CATEGORIA%TYPE;
    V_ID_PUBLICADOR  USUARIOS.ID_USUARIO%TYPE;
  BEGIN
    V_ID_CATEGORIA := GET_CATEGORIA_ID(P_CATEGORIA);
    V_ID_PUBLICADOR := GET_USUARIO_ID(P_PUBLICADOR_MAIL);

    SELECT MIN(C.ID_CONTENIDO)
      INTO V_ID_CONTENIDO
      FROM CONTENIDOS C
     WHERE UPPER(C.TITULO) = UPPER(P_TITULO);

    IF V_ID_CONTENIDO IS NULL THEN
      INSERT INTO CONTENIDOS (
        TITULO,
        TIPO_CONTENIDO,
        ANIO_LANZAMIENTO,
        DURACION_MINUTOS,
        SINOPSIS,
        CLASIFICACION_EDAD,
        ES_EXCLUSIVO,
        ID_CATEGORIA,
        ID_EMPLEADO_PUBLICADOR
      ) VALUES (
        P_TITULO,
        LOWER(P_TIPO),
        P_ANIO,
        P_DURACION_MIN,
        P_SINOPSIS,
        UPPER(P_CLASIFICACION),
        P_ES_EXCLUSIVO,
        V_ID_CATEGORIA,
        V_ID_PUBLICADOR
      );
    ELSE
      UPDATE CONTENIDOS
         SET TIPO_CONTENIDO = LOWER(P_TIPO),
             ANIO_LANZAMIENTO = P_ANIO,
             DURACION_MINUTOS = P_DURACION_MIN,
             SINOPSIS = P_SINOPSIS,
             CLASIFICACION_EDAD = UPPER(P_CLASIFICACION),
             ES_EXCLUSIVO = P_ES_EXCLUSIVO,
             ID_CATEGORIA = V_ID_CATEGORIA,
             ID_EMPLEADO_PUBLICADOR = V_ID_PUBLICADOR
       WHERE ID_CONTENIDO = V_ID_CONTENIDO;
    END IF;
  END;

  PROCEDURE UPSERT_CONTENIDO_GENERO (
    P_TITULO_CONTENIDO IN VARCHAR2,
    P_NOMBRE_GENERO    IN VARCHAR2
  )
  IS
    V_ID_CONTENIDO CONTENIDOS.ID_CONTENIDO%TYPE;
