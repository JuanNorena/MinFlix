  PROCEDURE UPSERT_REFERIDO (
    P_EMAIL_REFERENTE IN VARCHAR2,
    P_EMAIL_REFERIDO  IN VARCHAR2,
    P_ESTADO          IN VARCHAR2,
    P_DESCUENTO_PCT   IN NUMBER
  )
  IS
    V_ID_REFERENTE REFERIDOS.ID_USUARIO_REFERENTE%TYPE;
    V_ID_REFERIDO  REFERIDOS.ID_USUARIO_REFERIDO%TYPE;
    V_ID_REF_ROW   REFERIDOS.ID_REFERIDO%TYPE;
  BEGIN
    V_ID_REFERENTE := GET_USUARIO_ID(P_EMAIL_REFERENTE);
    V_ID_REFERIDO := GET_USUARIO_ID(P_EMAIL_REFERIDO);

    IF V_ID_REFERENTE = V_ID_REFERIDO THEN
      RAISE_APPLICATION_ERROR(-20146, 'Referente y referido no pueden ser el mismo usuario');
    END IF;

    BEGIN
      SELECT R.ID_REFERIDO
        INTO V_ID_REF_ROW
        FROM REFERIDOS R
       WHERE R.ID_USUARIO_REFERIDO = V_ID_REFERIDO;

      UPDATE REFERIDOS
         SET ID_USUARIO_REFERENTE = V_ID_REFERENTE,
             ESTADO_REFERIDO = UPPER(P_ESTADO),
             DESCUENTO_PCT = P_DESCUENTO_PCT,
             FECHA_REFERIDO = CURRENT_TIMESTAMP
       WHERE ID_REFERIDO = V_ID_REF_ROW;
    EXCEPTION
      WHEN NO_DATA_FOUND THEN
        INSERT INTO REFERIDOS (
          ID_USUARIO_REFERENTE,
          ID_USUARIO_REFERIDO,
          ESTADO_REFERIDO,
          DESCUENTO_PCT
        ) VALUES (
          V_ID_REFERENTE,
          V_ID_REFERIDO,
          UPPER(P_ESTADO),
          P_DESCUENTO_PCT
        );
    END;
  END;

  PROCEDURE UPSERT_FACTURA (
