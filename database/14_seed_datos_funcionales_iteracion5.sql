  BEGIN
    V_ID_CONTENIDO := GET_CONTENIDO_ID(P_TITULO_CONTENIDO);
    V_ID_GENERO := GET_GENERO_ID(P_NOMBRE_GENERO);

    SELECT COUNT(*)
      INTO V_EXISTE
      FROM CONTENIDOS_GENEROS CG
     WHERE CG.ID_CONTENIDO = V_ID_CONTENIDO
       AND CG.ID_GENERO = V_ID_GENERO;

    IF V_EXISTE = 0 THEN
      INSERT INTO CONTENIDOS_GENEROS (
        ID_CONTENIDO,
        ID_GENERO
      ) VALUES (
        V_ID_CONTENIDO,
        V_ID_GENERO
      );
    END IF;
  END;

