CREATE TABLE GENEROS (
  ID_GENERO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  NOMBRE VARCHAR2(80) NOT NULL,
  DESCRIPCION VARCHAR2(260) NULL,
  CONSTRAINT UK_GENEROS_NOMBRE UNIQUE (NOMBRE)
);

CREATE UNIQUE INDEX UK_GENEROS_NOMBRE_CI ON GENEROS (UPPER(NOMBRE));

CREATE TABLE CONTENIDOS_GENEROS (
  ID_CONTENIDO NUMBER NOT NULL,
  ID_GENERO NUMBER NOT NULL,
  FECHA_ASIGNACION TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
  CONSTRAINT PK_CONTENIDOS_GENEROS PRIMARY KEY (ID_CONTENIDO, ID_GENERO),
  CONSTRAINT FK_CONTENIDOS_GENEROS_CONTENIDO FOREIGN KEY (ID_CONTENIDO)
    REFERENCES CONTENIDOS(ID_CONTENIDO),
  CONSTRAINT FK_CONTENIDOS_GENEROS_GENERO FOREIGN KEY (ID_GENERO)
    REFERENCES GENEROS(ID_GENERO)
);

CREATE INDEX IDX_CONTENIDOS_GENEROS_GENERO
  ON CONTENIDOS_GENEROS (ID_GENERO, FECHA_ASIGNACION DESC);

CREATE TABLE TEMPORADAS (
  ID_TEMPORADA NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  ID_CONTENIDO NUMBER NOT NULL,
  NUMERO_TEMPORADA NUMBER(3,0) NOT NULL,
  TITULO VARCHAR2(180) NULL,
  DESCRIPCION VARCHAR2(1500) NULL,
  FECHA_ESTRENO DATE NULL,
  CONSTRAINT UK_TEMPORADAS_CONTENIDO_NUMERO UNIQUE (
    ID_CONTENIDO,
    NUMERO_TEMPORADA
  ),
  CONSTRAINT CK_TEMPORADAS_NUMERO CHECK (NUMERO_TEMPORADA >= 1),
  CONSTRAINT FK_TEMPORADAS_CONTENIDO FOREIGN KEY (ID_CONTENIDO)
    REFERENCES CONTENIDOS(ID_CONTENIDO)
);

CREATE OR REPLACE TRIGGER TRG_TEMPORADAS_TIPO_CONTENIDO_BIU
BEFORE INSERT OR UPDATE ON TEMPORADAS
FOR EACH ROW
DECLARE
  V_TIPO_CONTENIDO CONTENIDOS.TIPO_CONTENIDO%TYPE;
BEGIN
  SELECT C.TIPO_CONTENIDO
    INTO V_TIPO_CONTENIDO
    FROM CONTENIDOS C
   WHERE C.ID_CONTENIDO = :NEW.ID_CONTENIDO;

  IF V_TIPO_CONTENIDO NOT IN ('serie', 'podcast') THEN
    RAISE_APPLICATION_ERROR(
      -20051,
      'Solo contenidos tipo serie o podcast pueden tener temporadas'
    );
  END IF;
EXCEPTION
  WHEN NO_DATA_FOUND THEN
    RAISE_APPLICATION_ERROR(
      -20052,
      'No existe el contenido asociado para registrar temporada'
    );
END;
/

CREATE TABLE EPISODIOS (
  ID_EPISODIO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  ID_TEMPORADA NUMBER NOT NULL,
  NUMERO_EPISODIO NUMBER(4,0) NOT NULL,
  TITULO VARCHAR2(180) NOT NULL,
  DURACION_MINUTOS NUMBER(5,0) NULL,
  SINOPSIS VARCHAR2(1500) NULL,
  FECHA_ESTRENO DATE NULL,
  FECHA_ADICION TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
  CONSTRAINT UK_EPISODIOS_TEMPORADA_NUMERO UNIQUE (
    ID_TEMPORADA,
    NUMERO_EPISODIO
  ),
  CONSTRAINT CK_EPISODIOS_NUMERO CHECK (NUMERO_EPISODIO >= 1),
  CONSTRAINT CK_EPISODIOS_DURACION CHECK (
    DURACION_MINUTOS IS NULL OR DURACION_MINUTOS BETWEEN 1 AND 10000
  ),
  CONSTRAINT FK_EPISODIOS_TEMPORADA FOREIGN KEY (ID_TEMPORADA)
    REFERENCES TEMPORADAS(ID_TEMPORADA)
);

CREATE TABLE CONTENIDOS_RELACIONADOS (
  ID_RELACION NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  ID_CONTENIDO_ORIGEN NUMBER NOT NULL,
  ID_CONTENIDO_RELACIONADO NUMBER NOT NULL,
  TIPO_RELACION VARCHAR2(30) NOT NULL,
  DESCRIPCION VARCHAR2(260) NULL,
  FECHA_CREACION TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
  CONSTRAINT UK_CONT_REL_ORIGEN_DESTINO_TIPO UNIQUE (
    ID_CONTENIDO_ORIGEN,
    ID_CONTENIDO_RELACIONADO,
    TIPO_RELACION
  ),
  CONSTRAINT CK_CONT_REL_NO_REFLEXIVA CHECK (
    ID_CONTENIDO_ORIGEN <> ID_CONTENIDO_RELACIONADO
  ),
  CONSTRAINT CK_CONT_REL_TIPO CHECK (
    TIPO_RELACION IN (
      'SECUELA',
      'PRECUELA',
      'REMAKE',
      'SPIN_OFF',
      'VERSION_EXTENDIDA',
      'RELACIONADO'
    )
  ),
  CONSTRAINT FK_CONT_REL_CONTENIDO_ORIGEN FOREIGN KEY (ID_CONTENIDO_ORIGEN)
    REFERENCES CONTENIDOS(ID_CONTENIDO),
  CONSTRAINT FK_CONT_REL_CONTENIDO_DESTINO FOREIGN KEY (ID_CONTENIDO_RELACIONADO)
    REFERENCES CONTENIDOS(ID_CONTENIDO)
);

CREATE INDEX IDX_CONT_REL_DESTINO_TIPO
  ON CONTENIDOS_RELACIONADOS (ID_CONTENIDO_RELACIONADO, TIPO_RELACION);

CREATE OR REPLACE TRIGGER TRG_CONTENIDOS_RELACIONADOS_BI
BEFORE INSERT ON CONTENIDOS_RELACIONADOS
FOR EACH ROW
DECLARE
  V_RELACION_INVERSA NUMBER;
BEGIN
  SELECT COUNT(*)
    INTO V_RELACION_INVERSA
    FROM CONTENIDOS_RELACIONADOS CR
   WHERE CR.ID_CONTENIDO_ORIGEN = :NEW.ID_CONTENIDO_RELACIONADO
     AND CR.ID_CONTENIDO_RELACIONADO = :NEW.ID_CONTENIDO_ORIGEN
     AND CR.TIPO_RELACION = :NEW.TIPO_RELACION;

  IF V_RELACION_INVERSA > 0 THEN
    RAISE_APPLICATION_ERROR(
      -20053,
      'Ya existe una relacion inversa del mismo tipo entre estos contenidos'
    );
  END IF;
END;
/

COMMIT;
