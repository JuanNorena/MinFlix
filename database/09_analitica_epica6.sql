CREATE TABLE REPRODUCCIONES_PART (
  ID_REPRODUCCION          NUMBER GENERATED BY DEFAULT AS IDENTITY,
  ID_PERFIL                NUMBER        NOT NULL,
  ID_CONTENIDO             NUMBER        NOT NULL,
  PROGRESO_SEGUNDOS        NUMBER(10,0)  DEFAULT 0 NOT NULL,
  DURACION_TOTAL_SEGUNDOS  NUMBER(10,0)  NULL,
  PORCENTAJE_AVANCE        NUMBER(5,2)   DEFAULT 0 NOT NULL,
  ULTIMO_DISPOSITIVO       VARCHAR2(80)  NULL,
  ESTADO_REPRODUCCION      VARCHAR2(20)  DEFAULT 'EN_PROGRESO' NOT NULL,
  FECHA_INICIO             DATE          NOT NULL,
  FECHA_ULTIMO_EVENTO      DATE          NOT NULL
)
PARTITION BY RANGE (FECHA_INICIO) (
  PARTITION P_ANTES_2023 VALUES LESS THAN (DATE '2023-01-01'),
  PARTITION P_2023       VALUES LESS THAN (DATE '2024-01-01'),
  PARTITION P_2024       VALUES LESS THAN (DATE '2025-01-01'),
  PARTITION P_2025       VALUES LESS THAN (DATE '2026-01-01'),
  PARTITION P_2026_PLUS  VALUES LESS THAN (MAXVALUE)
);

-- Poblar la tabla particionada con los datos existentes
INSERT INTO REPRODUCCIONES_PART (
  ID_PERFIL, ID_CONTENIDO, PROGRESO_SEGUNDOS, DURACION_TOTAL_SEGUNDOS,
  PORCENTAJE_AVANCE, ULTIMO_DISPOSITIVO, ESTADO_REPRODUCCION,
  FECHA_INICIO, FECHA_ULTIMO_EVENTO
)
SELECT
  ID_PERFIL, ID_CONTENIDO, PROGRESO_SEGUNDOS, DURACION_TOTAL_SEGUNDOS,
  PORCENTAJE_AVANCE, ULTIMO_DISPOSITIVO, ESTADO_REPRODUCCION,
  TRUNC(FECHA_INICIO), TRUNC(FECHA_ULTIMO_EVENTO)
