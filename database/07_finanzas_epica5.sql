-- ----------------------------------------------------------------------------
-- Seccion 2: Tabla PAGOS
-- ----------------------------------------------------------------------------
CREATE TABLE PAGOS (
  ID_PAGO           NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  ID_USUARIO        NUMBER        NOT NULL,
  MONTO             NUMBER(10,2)  NOT NULL,
  MONTO_DESCUENTO   NUMBER(10,2)  DEFAULT 0 NOT NULL,
  MONTO_FINAL       NUMBER(10,2)  NOT NULL,
  METODO_PAGO       VARCHAR2(20)  NOT NULL,
  ESTADO_PAGO       VARCHAR2(20)  DEFAULT 'PENDIENTE' NOT NULL,
  FECHA_PAGO        TIMESTAMP     DEFAULT CURRENT_TIMESTAMP NOT NULL,
  FECHA_PERIODO     DATE          NOT NULL,
  DESCRIPCION       VARCHAR2(255) NULL,
  CONSTRAINT CK_PAGOS_METODO CHECK (
    METODO_PAGO IN ('TARJETA_CREDITO', 'TARJETA_DEBITO', 'PSE', 'NEQUI', 'DAVIPLATA')
  ),
  CONSTRAINT CK_PAGOS_ESTADO CHECK (
    ESTADO_PAGO IN ('PENDIENTE', 'EXITOSO', 'FALLIDO', 'REEMBOLSADO')
  ),
  CONSTRAINT CK_PAGOS_MONTO CHECK (MONTO > 0),
  CONSTRAINT CK_PAGOS_MONTO_FINAL CHECK (MONTO_FINAL >= 0),
  CONSTRAINT FK_PAGOS_USUARIO FOREIGN KEY (ID_USUARIO)
    REFERENCES USUARIOS(ID_USUARIO)
);

CREATE INDEX IDX_PAGOS_USUARIO_FECHA ON PAGOS (ID_USUARIO, FECHA_PAGO DESC);
CREATE INDEX IDX_PAGOS_ESTADO        ON PAGOS (ESTADO_PAGO);
CREATE INDEX IDX_PAGOS_PERIODO       ON PAGOS (FECHA_PERIODO);

-- ----------------------------------------------------------------------------
-- Seccion 3: Trigger auto-activacion de cuenta tras pago exitoso
-- ----------------------------------------------------------------------------
CREATE OR REPLACE TRIGGER TRG_PAGOS_ACTIVAR_CUENTA_AU
AFTER UPDATE OF ESTADO_PAGO ON PAGOS
FOR EACH ROW
BEGIN
  IF :NEW.ESTADO_PAGO = 'EXITOSO' AND :OLD.ESTADO_PAGO <> 'EXITOSO' THEN
    UPDATE USUARIOS
       SET ESTADO_CUENTA = 'ACTIVO',
           FECHA_CORTE   = ADD_MONTHS(:NEW.FECHA_PERIODO, 1)
     WHERE ID_USUARIO    = :NEW.ID_USUARIO
       AND ESTADO_CUENTA IN ('SUSPENDIDO', 'INACTIVO');
  END IF;
END;
/

-- ----------------------------------------------------------------------------
-- Seccion 4: Funcion FN_CALCULAR_MONTO
-- ----------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION FN_CALCULAR_MONTO (
  P_ID_USUARIO IN NUMBER
) RETURN NUMBER
IS
  V_PRECIO_PLAN       NUMBER(10,2);
  V_FECHA_SUSCRIPCION DATE;
  V_ID_REFERIDOR      NUMBER;
  V_MESES_ACTIVO      NUMBER;
  V_DESCUENTO         NUMBER := 0;
  V_MONTO_FINAL       NUMBER(10,2);
BEGIN
  SELECT P.PRECIO_MENSUAL, U.FECHA_SUSCRIPCION, U.ID_REFERIDOR
    INTO V_PRECIO_PLAN, V_FECHA_SUSCRIPCION, V_ID_REFERIDOR
    FROM USUARIOS U
    INNER JOIN PLANES P ON P.ID_PLAN = U.ID_PLAN
   WHERE U.ID_USUARIO = P_ID_USUARIO;

  V_MESES_ACTIVO := MONTHS_BETWEEN(SYSDATE, V_FECHA_SUSCRIPCION);

  IF V_MESES_ACTIVO > 24 THEN
    V_DESCUENTO := V_DESCUENTO + 15;
  ELSIF V_MESES_ACTIVO > 12 THEN
    V_DESCUENTO := V_DESCUENTO + 10;
  END IF;

  IF V_ID_REFERIDOR IS NOT NULL THEN
    V_DESCUENTO := V_DESCUENTO + 5;
  END IF;

  IF V_DESCUENTO > 20 THEN
    V_DESCUENTO := 20;
  END IF;

  V_MONTO_FINAL := ROUND(V_PRECIO_PLAN * (1 - V_DESCUENTO / 100), 2);
  RETURN V_MONTO_FINAL;
EXCEPTION
  WHEN NO_DATA_FOUND THEN
    RAISE_APPLICATION_ERROR(-20051, 'Usuario o plan no encontrado');
END;
/

-- ----------------------------------------------------------------------------
-- Seccion 5: Procedimiento SP_FACTURACION_MENSUAL con SAVEPOINT
-- Usa cursor REF para evitar restriccion de Oracle con variables en cursor estatico
-- ----------------------------------------------------------------------------
CREATE OR REPLACE PROCEDURE SP_FACTURACION_MENSUAL (
  P_FECHA_PERIODO IN DATE DEFAULT SYSDATE,
  P_METODO_PAGO   IN VARCHAR2 DEFAULT 'PSE'
)
IS
  TYPE T_USUARIO IS RECORD (
    ID_USUARIO    NUMBER,
    NOMBRE        VARCHAR2(120),
    EMAIL         VARCHAR2(180),
    PRECIO_MENSUAL NUMBER(10,2)
  );
  TYPE T_CUR IS REF CURSOR RETURN T_USUARIO;

  C_USUARIOS  T_CUR;
  R_USUARIO   T_USUARIO;

  V_MONTO_FINAL  NUMBER(10,2);
  V_DESCUENTO    NUMBER(10,2);
  V_PROCESADOS   NUMBER := 0;
  V_FALLIDOS     NUMBER := 0;
  V_INICIO_MES   DATE;
  V_FIN_MES      DATE;
  V_ESTADO_OK    VARCHAR2(20) := 'EXITOSO';
  V_ESTADO_FAIL  VARCHAR2(20) := 'FALLIDO';
  V_ESTADO_ACT   VARCHAR2(20) := 'ACTIVO';
  V_ERROR_MSG    VARCHAR2(200);
BEGIN
  V_INICIO_MES := TRUNC(P_FECHA_PERIODO, 'MM');
  V_FIN_MES    := ADD_MONTHS(V_INICIO_MES, 1);

  OPEN C_USUARIOS FOR
    SELECT U.ID_USUARIO, U.NOMBRE, U.EMAIL, P.PRECIO_MENSUAL
      FROM USUARIOS U
      INNER JOIN PLANES P ON P.ID_PLAN = U.ID_PLAN
     WHERE U.ESTADO_CUENTA = V_ESTADO_ACT
       AND NOT EXISTS (
         SELECT 1 FROM PAGOS PG
          WHERE PG.ID_USUARIO   = U.ID_USUARIO
            AND PG.ESTADO_PAGO  = V_ESTADO_OK
            AND PG.FECHA_PERIODO >= V_INICIO_MES
            AND PG.FECHA_PERIODO <  V_FIN_MES
       );

  LOOP
    FETCH C_USUARIOS INTO R_USUARIO;
    EXIT WHEN C_USUARIOS%NOTFOUND;

    SAVEPOINT SP_USUARIO;
    BEGIN
      V_MONTO_FINAL := FN_CALCULAR_MONTO(R_USUARIO.ID_USUARIO);
      V_DESCUENTO   := R_USUARIO.PRECIO_MENSUAL - V_MONTO_FINAL;

      INSERT INTO PAGOS (ID_USUARIO, MONTO, MONTO_DESCUENTO, MONTO_FINAL, METODO_PAGO, ESTADO_PAGO, FECHA_PERIODO, DESCRIPCION)
      VALUES (R_USUARIO.ID_USUARIO, R_USUARIO.PRECIO_MENSUAL, V_DESCUENTO, V_MONTO_FINAL, P_METODO_PAGO, V_ESTADO_OK, P_FECHA_PERIODO, 'Facturacion mensual');

      V_PROCESADOS := V_PROCESADOS + 1;

    EXCEPTION
      WHEN OTHERS THEN
        ROLLBACK TO SP_USUARIO;
        V_FALLIDOS  := V_FALLIDOS + 1;
        V_ERROR_MSG := SUBSTR(SQLERRM, 1, 200);

        INSERT INTO PAGOS (ID_USUARIO, MONTO, MONTO_DESCUENTO, MONTO_FINAL, METODO_PAGO, ESTADO_PAGO, FECHA_PERIODO, DESCRIPCION)
        VALUES (R_USUARIO.ID_USUARIO, R_USUARIO.PRECIO_MENSUAL, 0, R_USUARIO.PRECIO_MENSUAL, P_METODO_PAGO, V_ESTADO_FAIL, P_FECHA_PERIODO, V_ERROR_MSG);
    END;
  END LOOP;

  CLOSE C_USUARIOS;
  COMMIT;
  DBMS_OUTPUT.PUT_LINE('Exitosos: ' || V_PROCESADOS || ' | Fallidos: ' || V_FALLIDOS);
END;
/

-- ----------------------------------------------------------------------------
-- Seccion 6: Procedimiento SP_SUSPENDER_CUENTAS_MOROSAS
-- ----------------------------------------------------------------------------
CREATE OR REPLACE PROCEDURE SP_SUSPENDER_CUENTAS_MOROSAS
IS
  V_SUSPENDIDAS NUMBER := 0;
  V_ESTADO_OK   VARCHAR2(20) := 'EXITOSO';
BEGIN
  FOR R IN (
    SELECT U.ID_USUARIO
      FROM USUARIOS U
     WHERE U.ESTADO_CUENTA = 'ACTIVO'
       AND U.FECHA_CORTE < SYSDATE
       AND NOT EXISTS (
         SELECT 1 FROM PAGOS P
          WHERE P.ID_USUARIO  = U.ID_USUARIO
            AND P.ESTADO_PAGO = V_ESTADO_OK
            AND P.FECHA_PAGO  >= U.FECHA_CORTE - 30
       )
  ) LOOP
    UPDATE USUARIOS SET ESTADO_CUENTA = 'SUSPENDIDO' WHERE ID_USUARIO = R.ID_USUARIO;
    V_SUSPENDIDAS := V_SUSPENDIDAS + 1;
  END LOOP;

  COMMIT;
  DBMS_OUTPUT.PUT_LINE('Cuentas suspendidas: ' || V_SUSPENDIDAS);
END;
/

